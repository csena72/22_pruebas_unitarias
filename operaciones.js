
exports.sumarNumeros = (n1, n2) => {
    return (parseInt(n1) + parseInt(n2));
};

exports.restarNumeros = (n1, n2) => {
    return (parseInt(n1) - parseInt(n2));
};

exports.multiplicarNumeros = (n1, n2) => {
    return (parseInt(n1) * parseInt(n2));
};

exports.dividirNumeros = (n1, n2) => {
    return (parseInt(n1) / parseInt(n2));
};